package main

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"
)

type Icon struct {
	EnumDef string
	Name    string
	Path    string
}

//Assumes working dir of /IMG
const iconDir = "../../src/assets/icon"
const outputFile = "../../src/ui/icon_manifest.odin"

func main() {

	var icons []Icon

	err := filepath.WalkDir(iconDir, func(path string, d os.DirEntry, err error) error {
		if err != nil {
			return err
		}

		if d.IsDir() {
			return nil
		}

		ext := strings.ToLower(filepath.Ext(path))

		switch ext {
		case ".png", ".jpg", ".jpeg", ".bmp", ".webp":
		default:
			return nil
		}

		rel, err := filepath.Rel(iconDir, path)
		if err != nil {
			return err
		}

		rel = filepath.ToSlash(rel)

		base := filepath.Base(rel)
		name := strings.TrimSuffix(base, filepath.Ext(base))

		icons = append(icons, Icon{
			EnumDef: pathToEnum(rel),
			Name:    name,
			Path:    rel,
		})

		return nil
	})

	if err != nil {
		panic(err)
	}

	sort.Slice(icons, func(i, j int) bool {
		return icons[i].Path < icons[j].Path
	})

	var b strings.Builder

	b.WriteString("package ui\n\n")
	b.WriteString("// Code generated by image_manifest_gen.\n")
	b.WriteString("// DO NOT EDIT.\n\n")

	b.WriteString("Icon :: enum {\n")

	for _, icon := range icons {
		fmt.Fprintf(&b, "\t%s,\n", icon.EnumDef)
	}

	b.WriteString("}\n\n")

	fmt.Fprintf(&b, "ICON_PATHS :: [%d]string{\n", len(icons))

	for _, icon := range icons {
		fmt.Fprintf(&b, "\t%q,\n", icon.Path)
	}

	b.WriteString("}\n\n")

	fmt.Fprintf(&b, "ICON_NAMES :: [%d]string{\n", len(icons))

	for _, icon := range icons {
		fmt.Fprintf(&b, "\t%q,\n", icon.Name)
	}

	b.WriteString("}\n")

	if err := os.WriteFile(outputFile, []byte(b.String()), 0644); err != nil {
		panic(err)
	}
}

func pathToEnum(path string) string {
	path = strings.TrimSuffix(path, filepath.Ext(path))

	parts := strings.FieldsFunc(path, func(r rune) bool {
		switch r {
		case '/', '\\', '_', '-', ' ':
			return true
		}
		return false
	})

	var b strings.Builder

	for _, part := range parts {
		if part == "" {
			continue
		}

		b.WriteString(strings.ToUpper(part[:1]))

		if len(part) > 1 {
			b.WriteString(strings.ToLower(part[1:]))
		}
	}

	return b.String()
}
